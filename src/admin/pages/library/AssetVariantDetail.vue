    };

    auditHistory.value = [
        { type: 'sys', timestamp: '2026-03-07T12:00:00Z', message: 'Asset Render Initiated via Queue', actorId: 'System Orchestrator' },
        { type: 'sys', timestamp: '2026-03-07T12:05:00Z', message: 'Render Success. Pushed to CDN.', actorId: 'Job Worker 04' },
        { type: 'ops', timestamp: '2026-03-07T12:05:01Z', message: 'Status updated to Reviewing', actorId: 'Operations Bus' }
    ];
});

const isPreviewable = computed(() => variant.value && ['reviewing', 'approved', 'published'].includes(variant.value.status));

const hasAnyAction = computed(() => 
    (permissions.value.canCompare && isPreviewable.value) ||
    (permissions.value.canApprove && variant.value?.status === 'reviewing') ||
    (permissions.value.canPublish && variant.value?.status === 'approved')
);

const viewTicket = () => router.push(`/admin/queue/${variant.value?.queueItemId}`);
const launchComparison = () => router.push(`/admin/previews/compare?subject=${variant.value?.assetId}`);
const approveVariant = async () => { /* POST /workflow/approve mapping */ };
const publishVariant = async () => { /* POST /deployment/publish mapping */ };

const formatHash = (str: string) => str.length > 10 ? `...${str.slice(-6)}` : str;
